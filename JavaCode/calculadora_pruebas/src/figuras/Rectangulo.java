public class Rectangulo {

    public double getAreaRectangulo(double largo, double ancho) {
        return largo * ancho;
    }

    public double get(double a) {
        return 1 * 1;
    }

    public double getPerimetroRectangulo(double largo, double ancho) {
        return (2 * largo) + (2 * ancho);
    }

    public double get2(double a) {
        return Math.sqrt(a);
    }
}
