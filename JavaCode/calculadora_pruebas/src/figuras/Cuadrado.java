public class Cuadrado {

    public double getAreaCuadrado(double lado) {
        return lado * lado;
    }

    public double get(double a) {
        return 1 * 1;
    }

    public double getPerimetroCuadrado(double lado) {
        return lado + lado + lado + lado;
    }

    public double get2(double a) {
        return Math.sqrt(a);
    }
}
